def Lingkaran_Luas(r):
    # print("Luas Lingkaran     :", 3.14 * r * r)
    return 3.14 * r * r

def Lingkarang_Keliling(r):
    # print("Keliling Lingkaran :", 2 * 3.14 * r)
    return 2 * 3.14 * r

r = float(input("Masukkan jari-jari lingkaran : "))
    
    
# {Lingkaran_Luas(r)}
# {Lingkarang_Keliling(r)}

print(f"Luas lingkaran      : {Lingkaran_Luas(r)}")
print(f"Keliling lingkaran  : {Lingkarang_Keliling(r)}")